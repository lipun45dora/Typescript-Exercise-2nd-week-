let a = 3;  
let b = 2;  
  
switch (a+b){  
    case 1: {  
        console.log("a+b is 1.");  
        break;  
    }  
    case 2: {  
        console.log("a+b is 5.");  
        break;  
    }  
    case 3: {  
        console.log("a+b is 6.");  
        break;  
    }  
      
    default: {  
        console.log("a+b is 5.");  
        break;  
    }  
}  